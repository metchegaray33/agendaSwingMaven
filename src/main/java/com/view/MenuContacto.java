    private boolean validarCamposObligatorios(ActionEvent evt, ContactoVO empresaVO) {
        
      boolean valido=true;
				
				if (empresaVO.getRazonSocial().equals("")){
					
					valido=false;					
					JOptionPane.showMessageDialog((Component)evt.getSource(),
                            "La razon social no puede estar vacía",
                            "Ingreso Inválido",
                            JOptionPane.INFORMATION_MESSAGE);
				}
				

				if (empresaVO.getDomicilio().equals("")){
					
					valido=false;
					JOptionPane.showMessageDialog((Component)evt.getSource(),
                            "El domicilio no puede estar vacío",
                            "Ingreso Inválido",
                            JOptionPane.INFORMATION_MESSAGE);
				}

				
							
				return valido;

                                
    }

 

  
