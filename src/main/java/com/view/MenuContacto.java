    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        	ContactoVO empresaVO=new ContactoVO();					
				empresaVO.setId(new Integer(txtCodigoSolicitud.getText()).intValue());		
				empresaVO.setRazonSocial(txtRazonSocial.getText());
				empresaVO.setDomicilio(txtDomicilio.getText());
				
			

				try {
					
			
							
				empresaVO.setWeb(txtWeb.getText());
				
				empresaVO.setEmail(txtEmail.getText());				
				
				empresaVO.setTelefono(TXTTELEFONO.getText());
				
				empresaVO.setFax(txtfax.getText());
				
									
					ABMContactoController  aBMEmpresaController=new ABMContactoController();	
								
						boolean validoUpdate=validarCamposObligatorios(evt,empresaVO);	
				
						if (validoUpdate){
							aBMEmpresaController.insertOrUpdate(empresaVO);
							
							btnCancelar.doClick();
						}
						
					} catch (Exception e) {
						// TODO Auto-generated catch block
						e.printStackTrace();
						
						JOptionPane.showMessageDialog((Component)evt.getSource(),
	                            "Error al Guardar",
	                            "Se ha producido un error al Guardar",
	                            JOptionPane.INFORMATION_MESSAGE);
						
				}
                                
                                
    }//GEN-LAST:event_btnGuardarActionPerformed

